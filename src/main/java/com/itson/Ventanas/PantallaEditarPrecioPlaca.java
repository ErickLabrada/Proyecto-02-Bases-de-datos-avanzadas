    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        try {
            int index = jComboBox.getSelectedIndex();

            double costo = Double.parseDouble(jTextField1.getText());

            if (index == 0) {
                precioPlacaDAO.updatePrecioPlaca(entityManager, costo, precioPlacaDAO.getPrecioPlaca(entityManager).getPrecioVehiculoViejo());
            } else if (index == 1) {
                precioPlacaDAO.updatePrecioPlaca(entityManager, precioPlacaDAO.getPrecioPlaca(entityManager).getPrecioVehiculoNuevo(), costo);
            }
            showMessageDialog(null, "Precio actualizado ^^");
            mainScreen.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            showMessageDialog(null, "Ingrese un precio v√°lido");
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

