    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed

        Vigencia vigencia = (Vigencia) comboBoxVigencia.getSelectedItem();

        try {
            double costo = Double.parseDouble(jTextField1.getText());
            int index = comboBoxEstado.getSelectedIndex();

            if (index == 0) {
                precioLicenciaDAO.updatePrecioLicencia(entityManager, vigencia, precioLicenciaDAO.getPrecioLicencia(entityManager, vigencia).getPrecioNormal(), costo);
            } else if (index == 1) {
                precioLicenciaDAO.updatePrecioLicencia(entityManager, vigencia, costo, precioLicenciaDAO.getPrecioLicencia(entityManager, vigencia).getPrecioDiscapacidad());
            }
            
            showMessageDialog(null, "Precio actualizado ^^");
            
            mainScreen.setVisible(true);
            this.dispose();
            
        } catch (Exception e) {
            showMessageDialog(null, "Ingrese un precio v√°lido");
        }


    }//GEN-LAST:event_jToggleButton2ActionPerformed

